function F_c = C(ni, M)
    M11 = M(1:3,1:3);
    M22 = M(4:6,4:6);
    ni1 = ni(1:3);
    ni2 = ni(4:6);
    C11 = zeros(3,3);
    s1 = M11*ni1;
    C12 = - [0 -s1(3) s1(2); s1(3) 0 -s1(1); -s1(2) s1(1) 0];
    C21 = C12;
    s2 = M22*ni2;
    C22 = - [0 -s2(3) s2(2); s2(3) 0 -s2(1); -s2(2) s2(1) 0];
    F_c = [C11 C12; C21 C22]*ni;
end